#include <stdio.h>
#include <stdlib.h>
#include "main.h"
/**
* main - function prints the number of arguments passed into it.
* @argc: is a count of the arguments passed to the program
* @argv: an array of strings containing the arguments
* Return: 0 on success
*/
int main(int argc, char *argv[])
{
	argv = argv;

	printf("%d\n", argc - 1);
	return (0);
}
